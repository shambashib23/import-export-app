const express = require('express');
const multer = require('multer');
const multerS3 = require('multer-s3');
const aws = require('aws-sdk');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const userValidation = require('../../validations/user.validation');
const userController = require('../../controllers/user.controller');
const reviewController = require('../../controllers/review.controller');
const router = express.Router();



router
  .route('/')
  .post(auth('manageUsers'), validate(userValidation.createUser), userController.createUser)
  .get(auth('getUsers'), validate(userValidation.getUsers), userController.getUsers);

router.route('/:userId').get(auth('getUsers'), validate(userValidation.getUser), userController.getUser)
  .patch(auth('manageUsers'), validate(userValidation.updateUser), userController.updateUser)
  .delete(auth('manageUsers'), validate(userValidation.deleteUser), userController.deleteUser);


router.route('/:userId').post(auth(), reviewController.postReviewById);
module.exports = router;



// image upload in AWS
aws.config.update({
  secretAccessKey: process.env.AWS_SECRET_ACCESS_KEY,
  accessKeyId: process.env.AWS_ACCESS_KEY_ID,
  region: process.env.S3_REGION,
  s3BucketEndpoint: false,
});

const s3 = new aws.S3({
  // useDualstackEndpoint: true,
});
// Define the multer-s3 storage engine
const upload = multer({
  storage: multerS3({
    s3: s3,
    bucket: 'shipload-assets',
    acl: 'public-read', // Set the access control list (public-read means everyone can read the files)
    contentType: multerS3.AUTO_CONTENT_TYPE,
    metadata: function (req, file, cb) {
      cb(null, { fieldName: file.fieldname });
    },
    key: function (req, file, cb) {
      cb(null, Date.now().toString() + '-' + file.originalname);
    },
  }),
});

router.route('/upload/file').post(upload.single('file'), userController.uploadFile);


